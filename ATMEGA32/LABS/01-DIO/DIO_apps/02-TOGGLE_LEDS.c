#include "00-main.h"

#include "../DIO_driver/DIO_interface.h"
#include "util/delay.h"



void Toggle_Leds()
{
	//PORT_voidInit();
	DIO_voidSetPortDirection(DIO_u8PORTC, DIO_u8PORT_OUTPUT);

	while(1)
	{
		DIO_voidTogglePort(DIO_u8PORTC);
		_delay_ms(200);
	}
}
